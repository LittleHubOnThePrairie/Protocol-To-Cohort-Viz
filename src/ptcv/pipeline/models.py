"""Pipeline data models for PTCV-24 end-to-end orchestration.

PipelineResult carries all stage outputs and the lineage chain
sha256 values needed to verify ALCOA++ traceability from protocol
download through validated SDTM package.

Risk tier: MEDIUM — data pipeline orchestration models (no patient data).

Regulatory references:
- ALCOA++ Traceable: sha256 chain from protocol_sha256 through each stage
- ALCOA++ Original: each stage has its own run_id; pipeline_run_id groups
  the six lightweight stage-checkpoint lineage records
"""

from __future__ import annotations

import dataclasses
from typing import Optional, TYPE_CHECKING

if TYPE_CHECKING:
    from ..extraction.models import ExtractionResult
    from ..ich_parser.parser import ParseResult
    from ..soa_extractor.models import ExtractResult
    from ..sdtm.models import SdtmGenerationResult
    from ..sdtm.validation.models import ValidationResult


# Expected pipeline stage names (in order)
PIPELINE_STAGES = [
    "download",
    "extraction",
    "ich_parse",
    "soa_extraction",
    "sdtm_generation",
    "validation",
]


@dataclasses.dataclass
class StageCheckpoint:
    """Lightweight pipeline-level record written after each stage completes.

    Written to StorageGateway under the orchestrator's pipeline_run_id so
    that get_lineage(pipeline_run_id) returns exactly one record per stage.

    Attributes:
        stage: Pipeline stage name (one of PIPELINE_STAGES).
        stage_run_id: The run_id generated by the stage service.
        artifact_key: Primary artifact key produced by this stage.
        artifact_sha256: SHA-256 of the primary artifact.
        source_sha256: SHA-256 of the prior stage's primary artifact.
    """

    stage: str
    stage_run_id: str
    artifact_key: str
    artifact_sha256: str
    source_sha256: str


@dataclasses.dataclass
class LineageChainVerification:
    """Result of verifying the sha256 chain across all pipeline stages.

    Attributes:
        is_valid: True when every stage's source_sha256 matches the prior
            stage's artifact_sha256.
        stages_verified: Number of stage links verified.
        broken_at_stage: Name of the first stage where the chain breaks,
            or None if the chain is intact.
        details: Human-readable verification notes per stage.
    """

    is_valid: bool
    stages_verified: int
    broken_at_stage: Optional[str]
    details: list[str]


@dataclasses.dataclass
class PipelineResult:
    """End-to-end pipeline execution result for one protocol amendment.

    Carries the outputs from all 6 stages plus a lineage chain summary
    traceable from protocol download to validation report.

    Attributes:
        pipeline_run_id: UUID4 assigned by the orchestrator. Used as the
            run_id for the six stage-checkpoint lineage records, enabling
            get_lineage(pipeline_run_id) to return exactly six records.
        registry_id: Trial registry identifier.
        amendment_number: Protocol amendment version (e.g. "00").

        extraction_result: Output from PTCV-19 ExtractionService.
        parse_result: Output from PTCV-20 IchParser.
        soa_result: Output from PTCV-21 SoaExtractor.
        sdtm_result: Output from PTCV-22 SdtmService.
        validation_result: Output from PTCV-23 ValidationService.

        protocol_sha256: SHA-256 of the input protocol file (PTCV-18).
        stage_checkpoints: Ordered list of stage checkpoints.
        pipeline_timestamp_utc: ISO 8601 UTC timestamp of run start.
    """

    pipeline_run_id: str
    registry_id: str
    amendment_number: str

    extraction_result: Optional["ExtractionResult"]
    parse_result: Optional["ParseResult"]
    soa_result: Optional["ExtractResult"]
    sdtm_result: Optional["SdtmGenerationResult"]
    validation_result: Optional["ValidationResult"]

    protocol_sha256: str
    stage_checkpoints: list[StageCheckpoint]
    pipeline_timestamp_utc: str

    def verify_lineage_chain(self) -> LineageChainVerification:
        """Verify that sha256 values form an unbroken chain across stages.

        Returns:
            LineageChainVerification with is_valid=True when every stage's
            source_sha256 matches the prior stage's artifact_sha256.
        [PTCV-24 Scenario: Unbroken ALCOA++ lineage chain]
        """
        if not self.stage_checkpoints:
            return LineageChainVerification(
                is_valid=False,
                stages_verified=0,
                broken_at_stage=None,
                details=["No stage checkpoints recorded"],
            )

        details: list[str] = []
        prior_sha = self.protocol_sha256

        for cp in self.stage_checkpoints:
            if cp.stage == "download":
                # Download stage uses the input protocol_sha256
                prior_sha = cp.artifact_sha256
                details.append(f"  ✓ download: sha256={cp.artifact_sha256[:16]}…")
                continue

            if cp.source_sha256 != prior_sha:
                details.append(
                    f"  ✗ {cp.stage}: source_sha256 mismatch — "
                    f"expected {prior_sha[:16]}… got {cp.source_sha256[:16]}…"
                )
                return LineageChainVerification(
                    is_valid=False,
                    stages_verified=self.stage_checkpoints.index(cp),
                    broken_at_stage=cp.stage,
                    details=details,
                )

            details.append(
                f"  ✓ {cp.stage}: sha256={cp.artifact_sha256[:16]}… "
                f"← source={cp.source_sha256[:16]}…"
            )
            prior_sha = cp.artifact_sha256

        return LineageChainVerification(
            is_valid=True,
            stages_verified=len(self.stage_checkpoints),
            broken_at_stage=None,
            details=details,
        )

    @property
    def all_stages_complete(self) -> bool:
        """True when all 6 pipeline stages completed successfully."""
        return (
            self.extraction_result is not None
            and self.parse_result is not None
            and self.soa_result is not None
            and self.sdtm_result is not None
            and self.validation_result is not None
        )

    @property
    def stage_names_present(self) -> set[str]:
        """Set of stage names recorded in stage_checkpoints."""
        return {cp.stage for cp in self.stage_checkpoints}
